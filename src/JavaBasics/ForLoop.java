package JavaBasics;

public class ForLoop {

	public static void main(String[] args) {
		/*
		 * When you know exactly how many times you want to loop through 
		 * a block of code, use the for loop instead of a while loop
		 */
		for (int i = 1; i < 20; i++) 
		{
			System.out.println(i);
		}
	}
}
