package JavaBasics;

public class WhileLoop {  
	
	public static void main(String[] args) {  
	    /*
	     * The code in the loop will run, over and over again, 
	     * as long as a variable (i) is less than or equal to 10
	     */
		int i=1;  
	    while(i<=10){  
	        System.out.println(i);  
	    i++;
	   }  
	}  
} 